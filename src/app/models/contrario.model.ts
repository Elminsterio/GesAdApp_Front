export class Contrarios {

    nombreC: string;
    apellidosC: string;
    DocIdentificacionC: string;
    telefonoC: number;
    domicilioC: string;
    emailC: string;

    constructor( nombre, apellidos, identificacion, telefono, domicilio, email ) {

        this.nombreC = nombre;
        this.apellidosC = apellidos;
        this.DocIdentificacionC = identificacion;
        this.telefonoC = telefono;
        this.domicilioC = domicilio;
        this.emailC = email;

    }

}
